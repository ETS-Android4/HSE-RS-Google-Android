package com.example.task_22;

public class LinkData {
    private String name;
    private String phone;

    public LinkData() {
        name = "";
        phone = "";
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }
}
